public void calcularigc(double cuello, double altura, double cintura){
    double igc,mcm;
    igc=495/(1.0324-0.19077*Math.log10(cintura-cuello)+0.15456*(Math.log10(altura)))-450;
    txtigc.setText(String.valueOf(formatter.format(igc)));
    mcm=Double.parseDouble(txtpeso.getText())-(Double.parseDouble(txtpeso.getText())*(igc/100));
    txtmcm.setText(String.valueOf(formatter.format(mcm)));
}
