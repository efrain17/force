public void validarDias(){


if(contDias==diasmax){
  //  JOptionPane.showMessageDialog(null, "ejecu");
    if(!lunes.isSelected())lunes.setEnabled(false);
    if(!martes.isSelected())martes.setEnabled(false);
    if(!Miercoles.isSelected())Miercoles.setEnabled(false);
    if(!jueves.isSelected())jueves.setEnabled(false);
    if(!Viernes.isSelected())Viernes.setEnabled(false);
    if(!sabado.isSelected())sabado.setEnabled(false);
    if(fechaInic.getDate()!=null)cargar();
}
else {
    // JOptionPane.showMessageDialog(null, "activo");
    if(!lunes.isSelected())lunes.setEnabled(true);
    if(!martes.isSelected())martes.setEnabled(true);
    if(!Miercoles.isSelected())Miercoles.setEnabled(true);
    if(!jueves.isSelected())jueves.setEnabled(true);
    if(!Viernes.isSelected())Viernes.setEnabled(true);
    if(!sabado.isSelected())sabado.setEnabled(true);
}
universales();   
}
